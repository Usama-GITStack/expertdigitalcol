            $(this).prev(".card-header").css('border', 'none')
        }).on('hide.bs.collapse', function() {
            $(this).prev(".card-header").css('background', '#bdbdbd').find(".fa").removeClass(
                "fa-minus").addClass("fa-plus");
        });
    });
